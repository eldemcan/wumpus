
/**
 * this class shows location of objects in game board 
 * @author Can Eldem
 * @version 1.0
 * @since 
 */
public class Position {

	int dimension_x;   //shows x coordination of bat in gameboard
	int dimension_y;   //shows y coordination of bat in gameboard 
	
	public Position() {
		super();
	}

	public Position(int dimension_x, int dimension_y) {
		super();
		this.dimension_x = dimension_x;
		this.dimension_y = dimension_y;
	}
}
